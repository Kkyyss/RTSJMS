package ky.model;

public class Person {
	private String name;
	private int forwarding;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getForwarding() {
		return forwarding;
	}
	public void setForwarding(int forwarding) {
		this.forwarding = forwarding;
	}
}
